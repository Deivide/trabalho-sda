    private boolean hasUser(User user, List<User> list) {
        if (list != null) {
            Encryption encryption = new Encryption();
            for (User current : list) {
                current = encryption.decrypt(current);
                if (user.getUser().equals(current.getUser())) {
                    return true;
                }
            }
        }
        return false;
    }

