	private void actionBtnAdd(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actionBtnAdd
        user = new User(txtName.getText(), txtUser.getText().toLowerCase(), txtPassword.getText());
        if (!user.getName().trim().equals("") &&
                    !user.getUser().trim().equals("") &&
                    !user.getPassword().trim().equals("")) {
            //Encriptografa o objeto 
            Encryption encryption = new Encryption();
            User userSend = encryption.encrypt(user);

            try {
                Registry reg = LocateRegistry.getRegistry(Especification.MANAGER_ADDR, Especification.RMI_PORT);
                RemoteServiceInterface stub = (RemoteServiceInterface) reg.lookup(Especification.RMI_NAME);
                if (hasUser(user, stub.retrieveAllUsers())) {
                    labelError.setText("Usu치rio j치 existe.");
                    return;
                }
                stub.insertUser(userSend);

                dispose();
                if (previousConsult) {
                    new UsersDialog((java.awt.Frame)getParent(), true);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_actionBtnAdd

